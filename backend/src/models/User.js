class User {
    constructor(userId, accountId, fullName, gender, birthday, email, phone, avatar, department) {
        this.userId = userId;
        this.accountId = accountId;
        this.fullName = fullName;
        this.gender = gender;
        this.birthday = birthday;
        this.email = email;
        this.phone = phone;
        this.avatar = avatar;
        this.department = department;
    }
}